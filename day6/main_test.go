func TestTotalSpaces(t *testing.T) {
  t.Run("AoC Example", func(t *testing.T) {
    data := [][]byte{
      { '.', '.', '.', '.', '#', '.', '.', '.', '.', '.' },
      { '.', '.', '.', '.', '.', '.', '.', '.', '.', '#' },
      { '.', '.', '.', '.', '.', '.', '.', '.', '.', '.' },
      { '.', '.', '#', '.', '.', '.', '.', '.', '.', '.' },
      { '.', '.', '.', '.', '.', '.', '.', '#', '.', '.' },
      { '.', '.', '.', '.', '.', '.', '.', '.', '.', '.' },
      { '.', '#', '.', '.', '^', '.', '.', '.', '.', '.' },
      { '.', '.', '.', '.', '.', '.', '.', '.', '#', '.' },
      { '#', '.', '.', '.', '.', '.', '.', '.', '.', '.' },
      { '.', '.', '.', '.', '.', '.', '#', '.', '.', '.' },
    }

    result, err := totalGuardSpaces(data)
    assert.Nil(t, err)
    assert.Equal(t, 41, result)
  })
}

